class Calculator {
  // Add.
  sum(num1, num2) {
    return num1 + num2;
  }

  // Subtract
  subtract(num1, num2) {
    return num1 - num2;
  }

  // Divide.
  divide(num1, num2) {
    return num1 / num2;
  }

  // Multiply.
  multiply(num1, num2) {
    return num1 * num2;
  }
}

export default Calculator;
